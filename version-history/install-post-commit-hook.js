#!/usr/bin/env node
/* eslint-disable no-console */
const fs = require('node:fs');
const path = require('node:path');
const { spawnSync } = require('node:child_process');

const ROOT = path.resolve(__dirname, '..');

function runGit(args) {
  const result = spawnSync('git', args, {
    cwd: ROOT,
    encoding: 'utf8',
  });

  if (result.error) {
    throw result.error;
  }

  if (result.status !== 0) {
    throw new Error((result.stderr || 'git command failed').trim());
  }

  return (result.stdout || '').trim();
}

function installHook() {
  const gitDirRelative = runGit(['rev-parse', '--git-dir']);
  const gitDir = path.resolve(ROOT, gitDirRelative);
  const hooksDir = path.join(gitDir, 'hooks');
  const hookPath = path.join(hooksDir, 'post-commit');

  fs.mkdirSync(hooksDir, { recursive: true });

  const hookScript = `#!/bin/sh
# Auto-generated by version-history/install-post-commit-hook.js
ROOT_DIR="$(git rev-parse --show-toplevel 2>/dev/null)"
if [ -z "$ROOT_DIR" ]; then
  exit 0
fi

cd "$ROOT_DIR" || exit 0

if command -v node >/dev/null 2>&1; then
  node version-history/generate-version-history.js >/dev/null 2>&1 || exit 0
  git add documentation/assets/data/version-history.json >/dev/null 2>&1 || true
  echo "[version-history] updated documentation/assets/data/version-history.json"
fi
`;

  fs.writeFileSync(hookPath, hookScript, 'utf8');

  try {
    fs.chmodSync(hookPath, 0o755);
  } catch {
    // Ignore on environments where chmod is not applicable.
  }

  console.log(`[version-history] post-commit hook installed at ${hookPath}`);
  console.log('[version-history] on every commit, version-history.json will be regenerated and staged.');
}

try {
  installHook();
} catch (err) {
  console.error(`[version-history] failed to install hook: ${err instanceof Error ? err.message : String(err)}`);
  process.exit(1);
}
